    private void loadJurusan() {
    try {
        String sql = "SELECT * FROM jurusan";
        Connection conn = Koneksi.getConnection();

        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Koneksi database gagal");
            return;
        }

        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        cmbJurusan.removeAllItems(); // kosongkan dulu

        while (rs.next()) {
            int id = rs.getInt("id_jurusan");
            String nama = rs.getString("nama_jurusan");

            // format: ID - Nama
            cmbJurusan.addItem(id + " - " + nama);
        }

        // penting supaya tidak null
        if (cmbJurusan.getItemCount() > 0) {
            cmbJurusan.setSelectedIndex(0);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Load Jurusan error: " + e.getMessage());
    }
}


    private void tampilData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("NISN");
    model.addColumn("Nama");
    model.addColumn("JK");
    model.addColumn("Jurusan");
    model.addColumn("Status");

    try {
        String sql = """
            SELECT p.id_daftar, p.nisn, p.nama_lengkap, p.jk,
                   j.nama_jurusan, p.status
            FROM pendaftaran p
            JOIN jurusan j ON p.id_jurusan = j.id_jurusan
        """;

        Connection conn = Koneksi.getConnection();
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id_daftar"),
                rs.getString("nisn"),
                rs.getString("nama_lengkap"),
                rs.getString("jk"),
                rs.getString("nama_jurusan"),
                rs.getString("status")
            });
        }

        tblPendaftaran.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }
    
