    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
        String jurusan = cmbJurusan.getSelectedItem().toString();
        idJurusan = Integer.parseInt(jurusan.split(" - ")[0]);

        String sql = """
            INSERT INTO pendaftaran
            (nisn, nama_lengkap, jk, asal_sekolah, alamat, id_jurusan, tgl_daftar, status)
            VALUES (?,?,?,?,?,?,CURDATE(),?)
        """;

        Connection conn = Koneksi.getConnection();
        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, txtNisn.getText());
        ps.setString(2, txtNamaLengkap.getText());
        ps.setString(3, cmbJK.getSelectedItem().toString());
        ps.setString(4, txtAsalSekolah.getText());
        ps.setString(5, txtAlamat.getText());
        ps.setInt(6, idJurusan);
        ps.setString(7, cmbStatus.getSelectedItem().toString());

        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Pendaftaran berhasil");

        tampilData();
        resetForm();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

